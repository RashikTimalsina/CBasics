#include <stdio.h>


int main(){
	
	//logical Operators = || (OR) checks if at least one of the conditions is true..	
	
	float temp=20;	        			   
	
		
	
	if(temp<=0 || temp>=30){		//If atleast one condition meets then print this	
		
		printf("\nThe weather is good !");
		
	}		
	else{
		
		printf("\nThe weather is bad ! ");
	
	}
	
		return 0;
}
