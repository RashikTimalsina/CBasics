//Write a program in C, which swaps two variables

#include <stdio.h>

int main(){
	
	//Declaring variables;
	  int a = 10;
	  int b = 20;
	  int c;
	  //calculation to swap;
	  c = a;
	  a = b;
	  b = c;
	  
	  
	  //Print value
	  
	  printf("The value of a is %d\n", a);
	  printf("The value of b is %d\n", b);
	  
	  
	  
	  
	  
	  return 0;
	  
}
