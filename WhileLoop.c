#include <stdio.h>

int main(){
	
	
	//While Loop = repeats a section of code possibly unlimited times 
	//WHILE some conditions remain true
	//a while loop might not execute at all
	
	int i;
	
	i=0;				//initialization
	while(i<=10){		//while(condition)
		
		printf("%d\n" ,i);
		i++;			//increment/decrement
		
	}
	
	return 0;
	
}
