#include <stdio.h>
#include <math.h>					//import for Math Function

int main(){
	
	double A=sqrt(9);				//Square root of ...
	double B=pow(2,4);				//Power of a to b =here(a,b)
	int C=round(3.14);				//Round off value
	int D=ceil(3.14);				//Round up value
	int E=floor(3.99);				//Round down value
	double F=fabs(-100);			//Absolute value
	double G=log(3);				//Log value
	double H=sin(45);				//Sin function
	double I=cos(45);				//Cos function
	double J=tan(45);				//Tan function
	
	printf("\n%1f",A);
	printf("\n%1f",B);
	printf("\n%d",C);
	printf("\n%d",D);
	printf("\n%d",E);
	printf("\n%1f",F);
	printf("\n%1f",G);
	printf("\n%1f",H);
	printf("\n%1f",I);
	printf("\n%1f",J);
	
	
	return 0; 		
	
}
